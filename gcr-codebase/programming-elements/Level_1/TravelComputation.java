public class TravelComputation {
    public static void main(String[] args) {
        String Name="Eric";
        double C_to_V=156.6;
        double time1=4*60+4;
        double V_to_B=211.8;
        double time2=4*60+25;
        double total_distance= C_to_V + V_to_B;
        double total_time_taken=time1+time2;
        System.out.println("Name: "+Name);
        System.out.println("total_distance: "+total_distance);
        System.out.println("total_time: "+total_time_taken);
    }
    
}
